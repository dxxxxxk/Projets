#include <unistd.h>

void	ft_putchar(char c);

int		main(int ac, char **av)
{
	int		i;
	int		j;

	j = 1;
	while (j < ac)
	{
		i = 0;
		while (av[j][i] != '\0')
		{
			ft_putchar(av[j][i]);
			i++;
		}
		j++;
		ft_putchar('\n');
	}
	return (0);
}
