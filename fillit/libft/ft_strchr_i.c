#include <string.h>

int		ft_strchr_i(const char *s, int c)
{
	int		i;

	i = 0;
	if (s == NULL)
		return (-1);
	while (s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return ((s[i] == c) ? i : -1);
}
