char	*ft_strstr(const char *s1, const char *s2)
{
	char	*i;
	char	*j;

	if (*s2 == '\0')
		return ((char *)s1);
	while (*s1)
	{
		i = (char *)s1;
		j = (char *)s2;
		while (*j != '\0' && *j == *i)
		{
			j++;
			i++;
		}
		if (*j == '\0')
			return ((char *)s1);
		s1++;
	}
	return (0);
}
